def main():
    screen = turtle.Screen()
    t = turtle.Turtle()

    draw_K(t)
    draw_E(t)
    draw_V(t)
    draw_S(t)
    draw_E2(t)
    draw_R(t)

    screen.mainloop()

if __name__ == "__main__":
    main()
