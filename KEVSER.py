import turtle

def draw_K(t):
    t.penup()
    t.goto(-200,0)
    t.pendown()
    t.color("pink")
    t.pensize(20)
    t.left(90)
    t.forward(100)
    t.backward(50)
    t.right(45)
    t.forward(70)
    t.backward(70)
    t.right(90)
    t.forward(70)
    t.backward(70)
    t.right(45)
    t.forward(50)
    t.end_fill()
    t.left(90)
    t.end_fill()
    t.penup()
    t.home()

