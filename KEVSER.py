def draw_S(t):
    t.penup()
    t.goto(100,0)
    t.pendown()
    t.pensize(20)
    t.right(45)
    t.circle(30, 180)
    t.circle(-30, 180)
    t.end_fill()
    t.end_fill()
    t.penup()
